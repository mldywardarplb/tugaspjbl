<div class="resep-resep">
    <p class="judul-resep">Cara Membuat : </p>
    <p class="resep">
    1. Kukus singkong dalam dandang panas hingga matang, angkat lalu masukkan ke dalam mangkuk. Dalam keadaan panas, tumbuk singkong hingga halus dan lembut. </br></br>
    2. Tambahkan kelapa parut, gula pasir, margarin, dan ekstrak vanilla, tumbuk kembali hingga rata.</br></br>
    3. Bagi adonan menjadi 3 bagian, beri pasta stroberi dan pasta pandan pada 1 bagian adonan, biarkan 1 bagian tetap berwarna putih.</br></br>
    4. Ambil satu bagian adonan, masukkan ke dalam penggilingan getuk lindri/penggiling daging. Proses hingga adonan berbentuk seperti mie, potong-potong sesuai selera. Lakukan hal yang sama pada sisa adonan.</br></br>
    5. Penyajian: Letakkan Getuk dalam piring saji, tambahkan taburan di atas getuk. Sajikan segera.</br></br>
</p>
