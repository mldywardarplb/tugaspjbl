<div class="resep-resep">
    <p class="judul-resep">Cara Membuat : </p>
    <p class="resep">
        1. Bikin sambal kacang dahulu. Haluskan kacang tanah, cabai merah keriting, kemiri, cabai merah besar, gula merah, dan garam. 
        Tuang air. Masak sampai matang dan kental. <br><br>
        2. Ambil 150 gram sambal kacang, kecap manis, dan minyak goreng. Aduk rata. Celup ayam di dalamnya. Lumuri sampai rata. <br><br>
        3. Tusukkan ayam di tusuk sate. Bakar sampai setengah matang. Lumuri lagi dengan campuran kacang. Bakar lagi sampai matang. <br><br>
        4. Lanjutkan membuat sambal, blender cabai. Tambahkan garam. Aduk rata.<br><br>
        5. Sajikan sate bersama sambal kacang, pelengkap, dan sambalnya.<br><br>
</p>
