<div class="resep-resep">
    <p class="judul-resep">Cara Membuat: </p>
    <p class="resep">
        1. Panaskan 2 sdm minyak, tumis bumbu halus, daun salam, dan lengkuas hingga harum. Masukkan air kelapa, 
        gula merah, air asam jawa, garam, dan Bango Kecap Manis, aduk rata. <br><br>
        2. Masukkan tahu dan tempe. Masak di atas api sedang hingga mendidih. Angkat. Diamkan selama 1-2 jam hingga bumbu bacem meresap. <br><br> 
        3. Masak di atas api kecil hingga larutan bumbu habis meresap ke dalam tempe dan tahu. Angkat.  <br><br>
        4. Panaskan minyak, goreng tempe dan tahu hingga kecokelatan. Angkat. Sajikan. <br><br>
</p>
